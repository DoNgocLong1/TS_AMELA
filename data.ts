    if (!doc) {
  
      return null;
  
    }
  
  
  
    type U = Partial<Omit<T, "id"> & { _id: string }>;
  
    const result: U = {};
  
  
  
    for (const [key, value] of Object.entries(doc)) {
  
      if (key === from) {
  
        result[to] = doc[key];
  
      } else {
  
        result[key] = value;
  
      }
  
    }
  
  
  
    return result;
  
